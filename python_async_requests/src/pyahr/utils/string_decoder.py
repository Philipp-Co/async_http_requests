from abc import ABC, abstractmethod

#
# ---------------------------------------------------------------------------------------------------------------------
#
class StringDecoder(ABC):

    @abstractmethod
    def decode(self, data: bytes) -> str:
        pass

    pass
#
# ---------------------------------------------------------------------------------------------------------------------
#

class Utf8Decoder(StringDecoder):

    def __init__(self):
        super().__init__()
        pass

    def decode(self, data: bytes) -> str:
        return data.decode('utf-8', 'replace')

    pass
